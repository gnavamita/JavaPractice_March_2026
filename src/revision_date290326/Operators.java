package revision_date290326;

public class Operators {
    public static void main(String[] args) {
        int a = 1;
        int b= 2;
        System.out.println("a : "+a + " b : "+b);
        System.out.println("++a :" +  (++a));
        System.out.println("b++ :" +  (b++));
        System.out.println("a!=b " +  (a!=b));
        System.out.println("a % b " +  (a%b));
        System.out.println("a-- " +  (a--));
        System.out.println("--b " +  (--b));



    }
}
